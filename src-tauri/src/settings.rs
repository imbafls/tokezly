fn ensure_post_process_defaults(settings: &mut AppSettings) -> bool {
    let mut changed = false;
    for provider in default_post_process_providers() {
        // Use match to do a single lookup - either sync existing or add new
        match settings
            .post_process_providers
            .iter_mut()
            .find(|p| p.id == provider.id)
        {
            Some(existing) => {
                // The provider catalog is code-authoritative, so sync its metadata
                // onto existing installs (e.g. a renamed label). The base_url is
                // only synced for fixed providers — never for "custom", whose
                // base_url the user edits.
                if existing.label != provider.label {
                    existing.label = provider.label.clone();
                    changed = true;
                }
                if existing.models_endpoint != provider.models_endpoint {
                    existing.models_endpoint = provider.models_endpoint.clone();
                    changed = true;
                }
                if existing.supports_structured_output != provider.supports_structured_output {
                    existing.supports_structured_output = provider.supports_structured_output;
                    changed = true;
                }
                if !provider.allow_base_url_edit && existing.base_url != provider.base_url {
                    existing.base_url = provider.base_url.clone();
                    changed = true;
                }
            }
            None => {
                // Provider doesn't exist, add it
                settings.post_process_providers.push(provider.clone());
                changed = true;
            }
        }

        if !settings.post_process_api_keys.contains_key(&provider.id) {
            settings
                .post_process_api_keys
                .insert(provider.id.clone(), String::new());
            changed = true;
        }

        let default_model = default_model_for_provider(&provider.id);
        match settings.post_process_models.get_mut(&provider.id) {
            Some(existing) => {
                if existing.is_empty() && !default_model.is_empty() {
                    *existing = default_model.clone();
                    changed = true;
                }
            }
            None => {
                settings
                    .post_process_models
                    .insert(provider.id.clone(), default_model);
                changed = true;
            }
        }
    }

    changed
}

/// One-time migration that moves an install off an *unconfigured* cloud provider
/// onto the on-device engine.
///
/// Installs created before the on-device engine became the default may have a
/// cloud provider selected that was never given an API key, so the rewrite
/// silently no-ops. This corrects exactly those installs — once. It runs at most
/// once per install (guarded by `post_process_migrated_to_on_device`), so unlike
/// a per-read default it never fights a *deliberate* cloud selection that simply
/// has no key entered yet. A configured cloud key, or an explicit local choice
/// (on-device / custom Ollama / Apple Intelligence), is always preserved.
///
/// Returns `true` when it changed something to persist (always true the first
/// time it runs, since it at least records that the migration happened).
fn migrate_unconfigured_cloud_to_on_device(settings: &mut AppSettings) -> bool {
    if settings.post_process_migrated_to_on_device {
        return false;
    }
    settings.post_process_migrated_to_on_device = true;

    let selected = settings.post_process_provider_id.clone();
    let configured = settings
        .post_process_api_keys
        .get(&selected)
        .map(|key| !key.is_empty())
        .unwrap_or(false);
    let is_local_choice = selected == ON_DEVICE_PROVIDER_ID
        || selected == CLAUDE_CODE_PROVIDER_ID
        || selected == "custom"
        || selected == APPLE_INTELLIGENCE_PROVIDER_ID;
    if !is_local_choice && !configured {
        debug!(
            "One-time migration: refinement provider '{}' has no API key; switching to on-device",
            selected
        );
        settings.post_process_provider_id = ON_DEVICE_PROVIDER_ID.to_string();
    }
    true
}

/// One-time, self-healing upgrade of the built-in clean prompt.
///
/// The clean prompt lives in the user's settings store (seeded at first run),
/// so a code-only change to `CLEAN_PROMPT_TEXT` never reaches an existing
/// install. This refreshes the stored prompt to the current built-in text, but
/// *only* when it still matches the previous factory default — an install whose
/// clean prompt the user has edited keeps their version untouched. Idempotent:
/// once upgraded the text no longer matches `LEGACY_CLEAN_PROMPT_V1`, so it does
/// not run again.
fn upgrade_clean_prompt(settings: &mut AppSettings) -> bool {
    if let Some(prompt) = settings
        .post_process_prompts
        .iter_mut()
        .find(|p| p.id == CLEAN_PROMPT_ID)
    {
        if prompt.prompt == LEGACY_CLEAN_PROMPT_V1 && prompt.prompt != CLEAN_PROMPT_TEXT {
            debug!("Upgrading built-in clean prompt to the list-aware version");
            prompt.prompt = CLEAN_PROMPT_TEXT.to_string();
            return true;
        }
    }
    false
}

